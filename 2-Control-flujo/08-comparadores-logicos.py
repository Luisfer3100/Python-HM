# Los comparadores logicos nos ayudaran en la ejecución dentro de las estructuras de flujo, las cuales son caminos distintos que tomara nuestro programa dependiendo del resultado de una condición ya que estos nod retornan un valor booleano

# Comparadores

# Mayor que
print(5>2)

# Menor que
print(5<2)

# Mayor o igual que
print(5>=6)

# Menor o igual que
print(4<=4)

# Igual que 
print(5==5)

# Diferente que
print(5!=3)
