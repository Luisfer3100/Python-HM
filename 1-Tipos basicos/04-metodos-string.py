# upper
cadena = 'banana'
print(cadena.upper())

#lower
cadena = 'baNaNa'
print(cadena.lower())

# capitalize
cadena = 'banana'
print(cadena.capitalize())

#title
cadena = 'banana, manzana'
print(cadena.title())

#strip
cadena = '  banana, manzana  '
print(cadena.strip())

# lstrip
cadena = '  banana, manzana  '
print(cadena.lstrip())

#rstrip
cadena = '  banana, manzana  '
print(cadena.rstrip())

# find
cadena = 'banana, manzana'
print(cadena.find(','))

# replace
cadena = 'banana, manzana'
print(cadena.replace(',', ' y'))

# in
cadena = 'banana, manzana'
print('manzana' in cadena)

# not in
cadena = 'banana, manzana'
print('manzana' not in cadena)
