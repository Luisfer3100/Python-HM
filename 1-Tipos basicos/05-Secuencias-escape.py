# La secuencia de escape nos permite decirle a python que el siguiente caracter no lo tome como un carater especial si no como un caracter de string, al menos los escapes de caracteres no definidos en python:
cadena = 'I\'m Luis and i have a Television of 38"'
print(cadena)

# Como mencione, python tambien tiene secuencias de escapes definidas, como por ejemplo el escape \n, el cual significa newline, dara un salto de linea:
cadena = 'I\'m Luis \ni have a Television of 38"'
print(cadena)
