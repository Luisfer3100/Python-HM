# Las listas son un tipo de colección de datos en Python que permite almacenar varios valores de diferente tipo. Su sintaxis es con corchetes [], y cada elemento se separa por comas.
numeros = [1, 2, 3, 4]
print(numeros)
letras = ['a', 'b', 'c', 'd']
print(letras)
palabras = ['hola', 'como', 'estás']
print(palabras)
booleanos = [True, False, True, False]
print(booleanos)
matriz = [[1, 2, 3], [4, 5, 6]] # Una matriz en python es una lista de listas
print(matriz)
cero = [0] * 10 # Crea una lista de 10 elementos con valor 0
print(cero)
alfanumericos = numeros + letras # El valor de esta variable sera una lista concatenada de las listas numeros y letras
print(alfanumericos)
rango = list(range(1, 11)) # Crea una lista con los numeros del 1 al 10
print(rango)
chars = list('abcdefghijklmnopqrstuvwxyz') # Crea una lista con los caracteres del alfabeto
print(chars)
